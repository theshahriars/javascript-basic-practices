// Logical Operators [&&] [||] [!]

// Example 1

// Declaring variables
var a = true;
var b = false;

// And [&&]
console.log('(a && b) =>');
result = (a && b);
console.log(result);

// Or [||]
console.log('(a || b) =>');
result = (a || b);
console.log(result);

// Not [!]
console.log('(!a) =>');
result = (!a);
console.log(result);

console.log('(!b) =>');
result = (!b);
console.log(result);