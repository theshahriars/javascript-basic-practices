// Arithmatic Operators: [+] [-] [*] [/] [%] [++] [--]

// Example 1

// Declaring variables
var number1 = 9;
var number2 = 1;
var result;

// Addition using [+]
console.log('9 + 1 =');
result = number1 + number2;
console.log(result);

// Substraction using [-]
console.log('9 - 1 =');
result = number1 - number2;
console.log(result);

// Multiplication using [*]
console.log('9 * 1 =');
result = number1 * number2;
console.log(result);

// Division using [/]
console.log('9 / 1 =');
result = number1 / number2;
console.log(result);

// Reminder using [%]
console.log('9 % 1 =');
result = number1 % number2;
console.log(result);

// Declaring more variables
var a = 1, b = 2, c = 3;

console.log('a + b + c =');
result = a + b + c;
console.log(result);

// Declaring variables
var a = 0, b = 1;

// Increment using [++]
console.log('++a =');
result = ++a;
console.log(result);

// Decrement using [--]
console.log('--b =');
result = --b;
console.log(result);
