// Comparison Operators: [==] [!=] [<] [>] [<=] [>=]

// Example 1

// Declaring variables
var a = 5;
var b = 10;

// Equal [==]
console.log('(a == b) =>');
result = (a == b);
console.log(result);

// Not Equal [!=]
console.log('(a != b) =>');
result = (a != b);
console.log(result);

// Greater Than [>]
console.log('(a > b) =>');
result = (a > b);
console.log(result);

// Less Than [<]
console.log('(a < b) =>');
result = (a < b);
console.log(result);

// Greater Than Or Equal [>=]
console.log('(a >= b) =>');
result = (a >= b);
console.log(result);

// Greater Than Or Equal [<=]
console.log('(a <= b) =>');
result = (a <= b);
console.log(result);